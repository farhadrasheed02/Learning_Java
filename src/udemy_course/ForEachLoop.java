package udemy_course;
	public class ForEachLoop {
	
		public static void main(String[] args) {
			String  [] cars = {"bugatti","audi","Range Rover","Jeep","Baleno","Swift Desire"};
			Integer [] abc = {1,2,3,4};
			for(String i : cars)
			{
				System.out.println(i);
			}
			for(int i : abc)
			{
				System.out.println(i);
			}
				
		}
	
	}
